import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const contentDir = join(root, 'content/deep-dives');
const outputPath = join(root, 'src/lib/search/mdx-documents.generated.ts');
const checkOnly = process.argv.includes('--check');

function toTitle(source, slug) {
  const heading = source.match(/^#\s+(.+)$/m)?.[1]?.trim();
  if (heading) {
    return heading;
  }

  return slug
    .split('-')
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(' ');
}

function toPlainText(source) {
  return source
    .replace(/```[\s\S]*?```/g, ' ')
    .replace(/`([^`]+)`/g, '$1')
    .replace(/!\[([^\]]*)\]\([^)]+\)/g, '$1')
    .replace(/\[([^\]]+)\]\([^)]+\)/g, '$1')
    .replace(/^import\s+.+$/gm, ' ')
    .replace(/^export\s+.+$/gm, ' ')
    .replace(/[#>*_~|-]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

function toDescription(content) {
  const [sentence] = content.split(/(?<=[.!?])\s+/);
  return sentence && sentence.length <= 180
    ? sentence
    : `${content.slice(0, 177).trim()}...`;
}

const mdxSource = {
  label: 'THORChain Wiki deep-dive source',
  url: 'https://github.com/Reedtrullz/tcwiki/tree/main/content/deep-dives',
};

const documents = readdirSync(contentDir)
  .filter((file) => file.endsWith('.mdx'))
  .sort()
  .map((file) => {
    const slug = file.replace(/\.mdx$/, '');
    const slugPath = `/deep-dives/${slug}`;
    const source = readFileSync(join(contentDir, file), 'utf8');
    const content = toPlainText(source);
    return {
      id: `mdx:${slug}`,
      slug: slugPath,
      href: slugPath,
      type: 'deep-dive',
      title: toTitle(source, slug),
      description: toDescription(content),
      confidence: 'curated',
      reviewedAt: '2026-07-02',
      sources: [mdxSource],
      content,
    };
  });

const generated = `// Generated by scripts/generate-mdx-search.mjs. Do not edit by hand.
import type { SearchDoc } from '@/lib/search/registry';

export const MDX_SEARCH_DOCUMENTS: SearchDoc[] = ${JSON.stringify(documents, null, 2)};
`;

if (checkOnly) {
  const existing = readFileSync(outputPath, 'utf8');
  if (existing !== generated) {
    console.error(`${relative(root, outputPath)} is stale. Run npm run generate:search.`);
    process.exit(1);
  }
  console.log('MDX search document generation is current.');
} else {
  writeFileSync(outputPath, generated);
  console.log(`Wrote ${relative(root, outputPath)}.`);
}
